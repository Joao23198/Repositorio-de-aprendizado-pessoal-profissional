"1 - Strings como sequências"

"Strings são coleções ordenadas de caracteres"

"Cada caractere tem um índice(posição)"

"Podem ser percorridas com laços for"

"2 - Concatenação"

"Unir duas ou mais strings com o operador +"

mensagem = "Olá" + "João"

"3 - Interpolação (f-strings)"

"Inserir variáveis diretamente dentro da string"

nome = "João"
idade = 25
print(f"Meu nome é {nome} e tenho {idade} anos")

"4 - Fatiamento"

"Extrair partes da string usando índices"

texto = "Texto"
print(texto[0:3])
print(texto[-3:])

